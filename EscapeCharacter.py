#Single Quote = \'
#Backslash = \\
#New Line = \n
#Carriage Return = \r
#Tab = \t
#Backspace = \b
#Form Feed = \f
#octal Value = \ooo
#Hex Value = \xhh


#Single Quote = \'
txt = 'It\'s alright.'
print(txt) 

#Backslash = \\
txt = "This will insert one \\"
print(txt) 

#New Line = \n
txt = "Mahmudul\nHasan"
print(txt) 

#Carriage Return = \r
txt = "Mahmudul\rHasan"
print(txt) 

#Tab = \t
txt = "Mahmudul\tHasan"
print(txt)

#Backspace = \b
txt = "Mahmudul \bHasan"
print(txt)