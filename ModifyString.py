#Print All Uppercase
a = "Hello, World!"
print(a.upper())

#Print All Lowercase
a = "Hello, World!"
print(a.lower())


#Strip method remove all blank/whitespace 
a = " Hello, World! "
print(a.strip())

#Replace letter Note: Replace H to J
a = "Hello, World!"
print(a.replace("H", "J"))

a = "Mahmudul Hasan"
print(a.replace("s", "a"))

#split All letter
a = "Mahmudul Hasan"
print(a.split("."))



#String Concatenation
a = "Mahmudul"
b = "Hasan"
c = a + b
print(c)

#space Creating
a = "Mahmudul"
b = "Hasan"
c = a + " " + b
print(c)